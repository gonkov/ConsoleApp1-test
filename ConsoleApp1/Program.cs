    class Program
    {
        static void Main(string[] args)
        {
            for (int i = 0; i < 10; i++)
            {
                Console.WriteLine(i);
            }
            Console.ReadLine();
            int j = 0;
            while (j < 10)
            {
                j++;
                Console.WriteLine(j);
                
            }
            Console.ReadLine();
        }
    }
